static esp_err_t write_cmd(ssd1306_t *dev, uint8_t cmd)
{
    return hal_i2c_write(dev->bus, dev->addr, CTRL_CMD, &cmd, 1);
}

static esp_err_t write_cmd2(ssd1306_t *dev, uint8_t cmd, uint8_t arg)
{
    uint8_t buf[2] = { cmd, arg };
    return hal_i2c_write(dev->bus, dev->addr, CTRL_CMD, buf, sizeof(buf));
}

static esp_err_t write_data(ssd1306_t *dev, const uint8_t *data, size_t len)
{
    return hal_i2c_write(dev->bus, dev->addr, CTRL_DATA, data, len);
}

static esp_err_t send_init_sequence(ssd1306_t *dev)
{
    esp_err_t err;

#define CMD(c)       do { err = write_cmd(dev, c);       if (err) return err; } while(0)
#define CMD2(c, a)   do { err = write_cmd2(dev, c, a);   if (err) return err; } while(0)

    CMD(0xAE);          /* Display OFF (sleep mode)                          */

    CMD2(0xD5, 0x80);   /* Set display clock divide ratio / oscillator freq  */
    CMD2(0xA8, 0x3F);   /* Set multiplex ratio — 64 MUX                      */
    CMD2(0xD3, 0x00);   /* Set display offset — no vertical shift            */
    CMD(0x40);          /* Set start line to 0                               */

    CMD2(0x8D, 0x14);   /* Enable charge pump (internal VCC)                 */

    CMD2(0x20, 0x00);   /* Set memory addressing mode — horizontal           */

    CMD(0xA1);          /* Set segment remap — col 127 → SEG0               */
    CMD(0xC8);          /* Set COM output scan direction — remapped           */

    CMD2(0xDA, 0x12);   /* Set COM pins hardware config — alt, no remap     */
    CMD2(0x81, 0xCF);   /* Set contrast                                      */
    CMD2(0xD9, 0xF1);   /* Set pre-charge period                             */
    CMD2(0xDB, 0x40);   /* Set VCOMH deselect level                          */

    CMD(0xA4);          /* Entire display ON — output follows RAM            */
    CMD(0xA6);          /* Set normal display (not inverted)                 */

    CMD(0x2E);          /* Deactivate scroll                                 */
    CMD(0xAF);          /* Display ON                                        */

#undef CMD
#undef CMD2

    return ESP_OK;
}

esp_err_t ssd1306_init(ssd1306_t *dev, hal_i2c_bus_t *bus, uint8_t addr)
{
    if (!dev || !bus) {
        return ESP_ERR_INVALID_ARG;
    }
    if (dev->initialized) {
        ESP_LOGW(TAG, "already initialised");
        return ESP_OK;
    }

    dev->bus  = bus;
    dev->addr = addr;
    memset(dev->framebuf, 0x00, SSD1306_BUF_SIZE);

    esp_err_t err = send_init_sequence(dev);
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "init sequence failed (0x%x) — check wiring / address", err);
        return err;
    }

    err = ssd1306_flush(dev);
    if (err != ESP_OK) {
        return err;
    }

    dev->initialized = true;
    ESP_LOGI(TAG, "ready (addr=0x%02X)", addr);
    return ESP_OK;
}

esp_err_t ssd1306_deinit(ssd1306_t *dev)
{
    if (!dev || !dev->initialized) {
        return ESP_ERR_INVALID_STATE;
    }
    esp_err_t err = write_cmd(dev, 0xAE); /* Display OFF */
    dev->initialized = false;
    return err;
}

esp_err_t ssd1306_display_on(ssd1306_t *dev, bool on)
{
    if (!dev || !dev->initialized) return ESP_ERR_INVALID_STATE;
    return write_cmd(dev, on ? 0xAF : 0xAE);
}

esp_err_t ssd1306_set_contrast(ssd1306_t *dev, uint8_t contrast)
{
    if (!dev || !dev->initialized) return ESP_ERR_INVALID_STATE;
    return write_cmd2(dev, 0x81, contrast);
}

esp_err_t ssd1306_invert(ssd1306_t *dev, bool invert)
{
    if (!dev || !dev->initialized) return ESP_ERR_INVALID_STATE;
    return write_cmd(dev, invert ? 0xA7 : 0xA6);
}

void ssd1306_clear(ssd1306_t *dev)
{
    if (!dev) return;
    memset(dev->framebuf, 0x00, SSD1306_BUF_SIZE);
}

void ssd1306_fill(ssd1306_t *dev)
{
    if (!dev) return;
    memset(dev->framebuf, 0xFF, SSD1306_BUF_SIZE);
}

void ssd1306_set_pixel(ssd1306_t *dev, uint8_t x, uint8_t y)
{
    if (!dev) return;
    if (x >= SSD1306_WIDTH || y >= SSD1306_HEIGHT) return;

    uint16_t byte_idx = (y / 8) * SSD1306_WIDTH + x;
    uint8_t  bit_mask = 1u << (y % 8);

    dev->framebuf[byte_idx] |=  bit_mask;

}

bool ssd1306_get_pixel(const ssd1306_t *dev, uint8_t x, uint8_t y)
{
    if (!dev) return false;
    if (x >= SSD1306_WIDTH || y >= SSD1306_HEIGHT) return false;

    uint16_t byte_idx = (y / 8) * SSD1306_WIDTH + x;
    uint8_t  bit_mask = 1u << (y % 8);
    return (dev->framebuf[byte_idx] & bit_mask) != 0;
}

void ssd1306_draw_hline(ssd1306_t *dev, uint8_t x, uint8_t y,
                        uint8_t width)
{
    for (uint8_t i = 0; i < width; i++) {
        ssd1306_set_pixel(dev, x + i, y);
    }
}

void ssd1306_draw_vline(ssd1306_t *dev, uint8_t x, uint8_t y,
                        uint8_t height)
{
    for (uint8_t i = 0; i < height; i++) {
        ssd1306_set_pixel(dev, x, y + i);
    }
}

void ssd1306_draw_rect(ssd1306_t *dev, uint8_t x, uint8_t y,
                       uint8_t w, uint8_t h)
{
    ssd1306_draw_hline(dev, x,         y,         w);
    ssd1306_draw_hline(dev, x,         y + h - 1, w);
    ssd1306_draw_vline(dev, x,         y,         h);
    ssd1306_draw_vline(dev, x + w - 1, y,         h);
}

void ssd1306_fill_rect(ssd1306_t *dev, uint8_t x, uint8_t y,
                       uint8_t w, uint8_t h)
{
    for (uint8_t row = 0; row < h; row++) {
        ssd1306_draw_hline(dev, x, y + row, w);
    }
}

void ssd1306_draw_char(ssd1306_t *dev, uint8_t x, uint8_t y, char c)
{
	if (!dev) return;

	if (c < 0x20 || c > 0x7E) c = '?';

	const uint8_t *glyph = font5x7[(uint8_t)c];

	for (uint8_t col = 0; col < 5; col++) {
		uint8_t column_bits = glyph[col];
		for (uint8_t row = 0; row < 7; row++) {
			if (column_bits & (1u << row)) {
				ssd1306_set_pixel(dev, x + col, y + row);
			}
		}
	}
}

void ssd1306_draw_string(ssd1306_t *dev, uint8_t x, uint8_t y, const char *str)
{
	if (!dev || !str) return;

	while (*str) {
		if (x + 5 > SSD1306_WIDTH) break;
		ssd1306_draw_char(dev, x, y, *str);
		x   += 6;
		str++;
	}
}

esp_err_t ssd1306_flush(ssd1306_t *dev)
{
    if (!dev || !dev->bus) return ESP_ERR_INVALID_ARG;

    esp_err_t err;

    err = write_cmd2(dev, 0x21, 0x00);
    if (err != ESP_OK) return err;

    err = write_cmd(dev, SSD1306_WIDTH - 1);
    if (err != ESP_OK) return err;

    err = write_cmd2(dev, 0x22, 0x00);
    if (err != ESP_OK) return err;

    err = write_cmd(dev, SSD1306_PAGES - 1);
    if (err != ESP_OK) return err;

    return write_data(dev, dev->framebuf, SSD1306_BUF_SIZE);
}